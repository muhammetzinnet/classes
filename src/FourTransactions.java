public class FourTransactions {

    public int Gather(int number_1, int number_2){
        return number_1 + number_2;
    }

    public int Extraction(int number_1, int number_2){
        return number_1 - number_2;
    }

    public int Impact(int number_1, int number_2){
        return number_1 * number_2;
    }

    public double Divide(double number_1, double number_2){
            return number_1 / number_2;
    }
}
